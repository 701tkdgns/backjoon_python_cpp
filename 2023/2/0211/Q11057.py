conf = 10007
n = int(input())
dp = [[0 for _ in range(10)] for _ in range(n + 1)]

for i in range(10):
    dp[0][i] = 1

for i in range(1, n + 1):
    dp[i][0] = 1
    for j in range(1, 10):
        dp[i][j] = (dp[i-1][j] + dp[i][j - 1]) % conf

print(dp[n][9])

